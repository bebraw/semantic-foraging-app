import { mkdir, readFile, writeFile } from "node:fs/promises";

const cssPath = new URL("../.generated/styles.css", import.meta.url);
const modulePath = new URL("../src/generated/styles.ts", import.meta.url);

const css = await readFile(cssPath, "utf8");
const moduleSource = `// Generated by npm run build:css. Do not edit by hand.\nconst stylesheet = ${JSON.stringify(
  css,
)};\n\nexport default stylesheet;\n`;

await mkdir(new URL("../src/generated/", import.meta.url), { recursive: true });
await writeFile(modulePath, moduleSource, "utf8");
